package sadeh;

public class NapData {
	long averageNap = 0;
	int minNap = 0;
	int maxNap = 0;
	int numberNaps = 0;
	
	public long getAverageNap() {
		return averageNap;
	}
	public void setAverageNap(long averageNap) {
		this.averageNap = averageNap;
	}
	public int getMinNap() {
		return minNap;
	}
	public void setMinNap(int minNap) {
		this.minNap = minNap;
	}
	public int getMaxNap() {
		return maxNap;
	}
	public void setMaxNap(int maxNap) {
		this.maxNap = maxNap;
	}
	public int getNumberNaps() {
		return numberNaps;
	}
	public void setNumberNaps(int numberNaps) {
		this.numberNaps = numberNaps;
	}
}
